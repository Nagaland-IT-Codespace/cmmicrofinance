    public function storeFile($file_name,$data,$request)
    {
      if($request->hasFile($file_name))
        {
          $ext = $request->file($file_name)->extension();
          $data->update([
            $file_name => $request->file($file_name)->storeAs('public/gallery/',$file_name.'_'.$data->id.".".$ext),
          ]);
        }
        return;
    }
